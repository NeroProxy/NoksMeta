				<p>Noks é um aplicativo de cunho estudantil, voltado para maior organização do aluno em relação às suas notas e afazeres semanais. O aplicativo conta com várias funcionalidades que vão ajudar você em sua vida escolar e facilitar a administração de sua pontuação. Ele também é parte do trabalho de conclusão do curso de Informática no Centro Federal de Educação Tecnológica de Minas Gerais.</p>
				<p>A ideia do projeto surgiu à partir da concepção de que as turmas e os alunos em geral tem dificuldade em se organizar quanto às suas notas, atividades escolares e materiais para estudo. O Noks promete acabar com tudo isso. Ele promete uma interface simples e fácil de ser utilizada, para melhor administração do aluno, acesso ao cronograma semanal da própria turma e aos arquivos da turma.</p>
				<p>Futuramente Noks visa outras funcionalidades, como o fórum próprio da turma. Esse fórum seria único para cada turma e a mesma poderia exibir e esclarecer as suas dúvidas em relação a certa matéria. Cada aluno poderia postar ilimitadas dúvidas e responder igualmente, porém somente o administrador da turma poderia gerenciar o fórum. Além disso, estamos projetando também o desenvolvimento de uma caixa de mensagens de cada aluno, para que um possa se comunicar com o outro.</p>
